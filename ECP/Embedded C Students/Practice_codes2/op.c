#include<stdio.h>

int main(void)
{

int a = 10, b = 5;
int sum = a + b;        // sum = 15
printf("sum = %d\n", sum);
int diff = a - b;       // diff = 5
printf("sum = %d\n", diff);

int product = a * b;    // product = 50
printf("sum = %d\n", product);

int quotient = a / b;   // quotient = 2
printf("sum = %d\n", quotient);

int remainder = a % b;  // remainder = 0
printf("sum = %d\n", remainder);

return 0;
}